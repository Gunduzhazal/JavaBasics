package JavaBasics;

public class ForLoop {

	public static void main(String[] args) {
		
			for (int i = 1; i <= 5; i++) {
			System.out.print("Hello" +" ");
			}
			
			System.out.println();
			
			for (int i = 1; i <= 20; i++) {
			System.out.print(i + " ");
			}
			
			System.out.println();
			
			for(int i=30; i>=10; i--) {
			System.out.print(i+" ");
			}
			
			System.out.println();
			
			for(int i=0; i<=10; i+=2) {
			System.out.print(i+" ");
			}
			
			System.out.println();

			for(int i=10; i<=25; i+=5) {
			System.out.print(i+" ");
			}
			
			System.out.println();
			
			int sum=0;
			
			for(int i=1; i<=6; i++) {
			sum+=i;
			}
			System.out.println(sum);
			
			
			}
	}


