package JavaBasics;

public class DoWhile {

	public static void main(String[] args) {
		
		
		int num = 1;
		
		while(num<=2) {
			
		System.out.println("I am while loop");
		num++;
		
		}
		
		
		
		int num1 = 1;
		
		do {
			
		System.out.println("I am do while loop");
		num1++;
		
		}while(num1<=2);

	}

}
